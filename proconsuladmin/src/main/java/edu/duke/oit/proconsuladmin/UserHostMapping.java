package edu.duke.oit.proconsuladmin;

public class UserHostMapping {
	private String eppn;
	private String oudn;
	private String fqdn;
	public String getEppn() {
		return eppn;
	}
	public void setEppn(String eppn) {
		this.eppn = eppn;
	}
	public String getOudn() {
		return oudn;
	}
	public void setOudn(String oudn) {
		this.oudn = oudn;
	}
	public String getFqdn() {
		return fqdn;
	}
	public void setFqdn(String fqdn) {
		this.fqdn = fqdn;
	}
	
}
